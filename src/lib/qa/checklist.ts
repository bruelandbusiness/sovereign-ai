/**
 * Sovereign Empire QA Checklist Module
 *
 * Codifies every validation check from the QA specification into typed,
 * queryable data structures. Pure TypeScript with no database access or
 * external imports. Each QaCheck maps to a single testable assertion that
 * must pass before the platform (or a specific phase) is considered ready.
 *
 * Sections 1-7 correspond to build phases 1-7.
 * Sections 8-10 are recurring audits (full system, pre-onboard, monthly).
 */

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export type QaVerdict = "pass" | "warning" | "fail";

export type QaSection =
  | "foundation"
  | "discovery"
  | "outreach"
  | "billing_client_ops"
  | "reporting_dashboard"
  | "acquisition"
  | "advanced_features"
  | "full_system"
  | "pre_onboard"
  | "monthly_health";

export type QaSubsection = string;

export interface QaCheck {
  id: string;
  section: QaSection;
  subsection: QaSubsection;
  label: string;
  critical: boolean;
  automatable: boolean;
  order: number;
}

export interface QaCheckResult {
  checkId: string;
  verdict: QaVerdict;
  detail?: string;
}

export interface QaSectionMeta {
  section: QaSection;
  name: string;
  description: string;
  phase: number | null;
  runWhen: string;
}

