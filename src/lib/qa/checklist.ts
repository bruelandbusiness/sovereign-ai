/** Return all checks belonging to a given section. */
export function getChecksBySection(section: QaSection): QaCheck[] {
  return QA_CHECKS.filter((c) => c.section === section);
}

/** Return every check marked as critical. */
export function getCriticalChecks(): QaCheck[] {
  return QA_CHECKS.filter((c) => c.critical);
}

/** Return every check that can be verified programmatically. */
export function getAutomatableChecks(): QaCheck[] {
  return QA_CHECKS.filter((c) => c.automatable);
}

/** Look up section metadata by section key. */
export function getSectionMeta(section: QaSection): QaSectionMeta | undefined {
  return QA_SECTIONS.find((s) => s.section === section);
}

/** Return aggregate counts across the entire checklist. */
export function getCheckCount(): {
  total: number;
  critical: number;
  automatable: number;
} {
  return {
    total: QA_CHECKS.length,
    critical: QA_CHECKS.filter((c) => c.critical).length,
    automatable: QA_CHECKS.filter((c) => c.automatable).length,
  };
}
